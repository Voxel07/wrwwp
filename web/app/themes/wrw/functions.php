/**
 * Enqueue scripts and styles.
 */
function airsoft_theme_enqueue_styles() {
	// Basic structural styles (resets handled by MUI CssBaseline, so we only need to enqueue our custom compiled style if it exists).
	wp_enqueue_style( 'airsoft-style', get_stylesheet_uri(), array(), wp_get_theme()->get( 'Version' ) );
	wp_enqueue_style( 'wrw-fonts', 'https://fonts.googleapis.com/css2?family=Poppins:wght@400;500;700&family=Roboto:wght@400;500;700&display=swap', array(), wp_get_theme()->get( 'Version' ) );

	// Unenqueue vanilla-tilt as it's not needed with React/MUI.
	// React / Vite Bundle.
	$dist_url  = get_template_directory_uri() . '/dist';
	$dist_path = get_template_directory() . '/dist';

	// We configured Vite to output strictly to assets/main.js and potentially assets/vendor.js.
	if ( file_exists( $dist_path . '/assets/vendor.js' ) ) {
		wp_enqueue_script( 'wrw-react-vendor', $dist_url . '/assets/vendor.js', array(), wp_get_theme()->get( 'Version' ), true );
	}

	if ( file_exists( $dist_path . '/assets/main.js' ) ) {
		wp_enqueue_script( 'wrw-react-app', $dist_url . '/assets/main.js', array( 'wrw-react-vendor' ), wp_get_theme()->get( 'Version' ), true );
		// If there's a CSS file generated by Vite.
		if ( file_exists( $dist_path . '/assets/main.css' ) ) {
			wp_enqueue_style( 'wrw-react-style', $dist_url . '/assets/main.css', array(), wp_get_theme()->get( 'Version' ) );
		}
	}
}
add_action( 'wp_enqueue_scripts', 'airsoft_theme_enqueue_styles' );

// Allow modules for scripts enqueued by Vite.
add_filter(
	'script_loader_tag',
	function ( $tag, $handle, $src ) {
		if ( in_array( $handle, array( 'wrw-react-app', 'wrw-react-vendor' ), true ) ) {
			// phpcs:ignore WordPress.WP.EnqueuedResources.NonEnqueuedScript
			return '<script type="module" src="' . esc_url( $src ) . '"></script>';
		}
		return $tag;
	},
	10,
	3
);

